public class LengthModel {
    private double firstLength;
    private int firstSpinner;
    private double secondLength;
    private int secondSpinner;

    public double getFirstLength() {
        return firstLength;
    }

    public void setFirstLength(double firstLength) {
        this.firstLength = firstLength;
    }

    public int getFirstSpinner() {
        return firstSpinner;
    }

    public void setFirstSpinner(int firstSpinner) {
        this.firstSpinner = firstSpinner;
    }

    public double getSecondLength() {
        return secondLength;
    }

    public void setSecondLength(double secondLength) {
        this.secondLength = secondLength;
    }

    public int getSecondSpinner() {
        return secondSpinner;
    }

    public void setSecondSpinner(int secondSpinner) {
        this.secondSpinner = secondSpinner;
    }
}
