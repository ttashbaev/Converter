public class MassModel {
    private double firstMass;
    private int firstMassSpinner;
    private double secondMass;
    private int secondMassSpinner;

    public double getFirstMass() {
        return firstMass;
    }

    public void setFirstMass(double firstMass) {
        this.firstMass = firstMass;
    }

    public int getFirstMassSpinner() {
        return firstMassSpinner;
    }

    public void setFirstMassSpinner(int firstMassSpinner) {
        this.firstMassSpinner = firstMassSpinner;
    }

    public double getSecondMass() {
        return secondMass;
    }

    public void setSecondMass(double secondMass) {
        this.secondMass = secondMass;
    }

    public int getSecondMassSpinner() {
        return secondMassSpinner;
    }

    public void setSecondMassSpinner(int secondMassSpinner) {
        this.secondMassSpinner = secondMassSpinner;
    }
}
